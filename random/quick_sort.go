func QuickSort(items []int) (res []int) {
	if len(items) < 2 {
		return items
	}

	return quickSort(items, 0, len(items)-1)
}

func quickSort(arr []int, low, high int) []int {
	if low < high {
		var p int
		arr, p = partition(arr, low, high)
		arr = quickSort(arr, low, p-1)
		arr = quickSort(arr, p+1, high)
	}
	return arr
}

